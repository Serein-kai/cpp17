/*6.编写一个程序，仅使用条件运算符确定输入的整数是否是 20 或小于 20;
大于 20 且不大于 30大于 30 但不超过 100:或者大于 100。
条件运算符
c=a>b?a:b;

*/

#include <iostream>
using namespace std;

int main()
{
    int a{};
    cout<<"请输入一个整数：";
    cin>>a;
    cout<<"你输入的数是："<<(a<=20?"是20或小于20":(a<=30?"是大于20不大于30":(a<=100?"大于30但不超过100":"大于100")))<<endl;

    return 0;
}
